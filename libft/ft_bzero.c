#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*ptr;

	i = 0 ;
	if (!n)
		return ;
	while (i < n)
	{
		ptr = s + i;
		*ptr = 0;
		i++;
	}
}
